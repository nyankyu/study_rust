fn main() {
    let mut x = 5;
    println!("The value of x is: {}", x);
    x = 6;
    println!("The value of x is: {}", x);

    let y = 7;
    let y = y + 10;
    let y = y * 50;
    let y = y / 3;
    println!("y is: {}", y);

    let spaces = "     ";
    let spaces = spaces.len();
    println!("spaces: {}", spaces);
}
